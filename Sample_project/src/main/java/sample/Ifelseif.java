package sample;

public class Ifelseif {
	public static void main(String args[])
	{
		int a=1;
		if(a>0)
		{
			System.out.println(a+ " is Positive");	
		}
		else if(a<0)
		{
			System.out.print(a+" is Negative");	
		}
		else
		{
	 		System.out.println("Number is zero");
		}
		
	}
}
